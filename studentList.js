function getIndexFromID(collegeID){
    if(collegeID.endsWith("-D")){
        collegeID = collegeID.substring(0, collegeID.length - 2);
    }
    return collegeIDList.indexOf(collegeID);
}

function getNameFromID(collegeID){
    var index = getIndexFromID(collegeID);
    if(index == -1){
        return "Invalid College ID";
    }
    var name = nameList[index];
    if(collegeID.endsWith("-D"))
        name += " (Duplicate)";
    return name;
}

function displayName(){
    var collegeID = document.getElementById("idInput").value;
    var name = getNameFromID(collegeID);
    document.getElementById("nameLabel").innerHTML = name;
}